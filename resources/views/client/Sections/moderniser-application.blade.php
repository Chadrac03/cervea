                <li><i class="fas fa-caret-right"></i> la mesure de la dette technique et l’identification des failles de sécurité,</li>
                <li><i class="fas fa-caret-right"></i> le degré de couverture des tests, de la documentation, du processus de développement et de déploiement.</li>
