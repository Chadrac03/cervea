                <p> 6 - <strong class="font-weight-extra-bold">Évaluation de la couverture des tests, de la documentation, du processus de développement et de déploiement.</strong> Cette évaluation est faite par rapport à l’état de l’art, aux bonnes pratiques et à notre expertise.</p>
